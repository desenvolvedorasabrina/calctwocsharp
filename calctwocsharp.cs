<summary>
// Função que calcula a média dos valores passados como parâmetro
<param name="n1">Primeiro Valor</param>
<param name="n2">Segundo Valor</param>
<returns>Média dos valores</returns>
</summary>

namespace calculator
{
    public class calcula
    {
        public double Media(double n1, double n2)
        {
            return (n1 + n2) /2;
        }
    }
}
